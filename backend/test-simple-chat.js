// Simple test for chat API
const http = require('http');

const postData = JSON.stringify({
  message: 'What is coding?',
  skill: 'Programming'
});

const options = {
  hostname: 'localhost',
  port: 5000,
  path: '/api/chat/message',
  method: 'POST',
  headers: {
    'Content-Type': 'application/json',
    'Content-Length': Buffer.byteLength(postData)
  }
};

console.log('ğŸ§ª Testing chat API...');

const req = http.request(options, (res) => {
  console.log(`Status: ${res.statusCode}`);
  
  let data = '';
  res.on('data', (chunk) => {
    data += chunk;
  });
  
  res.on('end', () => {
    try {
      const response = JSON.parse(data);
      console.log('âœ… Response received:');
      console.log('Success:', response.success);
      if (response.success) {
        console.log('AI Response:', response.data.aiResponse.content.substring(0, 200) + '...');
        console.log('Generated by:', response.data.aiResponse.generatedBy);
      } else {
        console.log('Error:', response.message);
      }
    } catch (e) {
      console.log('âŒ Invalid JSON response:', data);
    }
  });
});

req.on('error', (e) => {
  console.error('âŒ Request error:', e.message);
});

req.write(postData);
req.end();
