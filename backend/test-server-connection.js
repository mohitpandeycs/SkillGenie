// Test if the server is running and responding

const axios = require('axios');

async function testServerConnection() {
  console.log('🔌 Testing server connection...');
  
  try {
    // Test basic server health
    const healthResponse = await axios.get('http://localhost:5000/api/health');
    console.log('✅ Server is running:', healthResponse.status);
  } catch (error) {
    console.log('❌ Server health check failed:', error.message);
  }
  
  try {
    // Test analytics endpoint
    console.log('\n📊 Testing analytics endpoint...');
    const analyticsResponse = await axios.get('http://localhost:5000/api/analytics/market/dynamic?skill=Mobile Development&location=India');
    
    if (analyticsResponse.data.success) {
      console.log('✅ Analytics endpoint working!');
      console.log('Generated by:', analyticsResponse.data.generatedBy);
      console.log('Skill:', analyticsResponse.data.data.skill);
      console.log('Salary:', analyticsResponse.data.data.marketOverview?.averageSalary);
      console.log('Growth:', analyticsResponse.data.data.marketOverview?.growthRate);
    } else {
      console.log('❌ Analytics endpoint failed');
    }
  } catch (error) {
    console.log('❌ Analytics test failed:', error.message);
  }
  
  try {
    // Test roadmap endpoint
    console.log('\n🗺️ Testing roadmap endpoint...');
    const roadmapResponse = await axios.post('http://localhost:5000/api/roadmaps/generate/dynamic', {
      skill: 'Mobile Development',
      level: 'intermediate',
      duration: '3-6 months'
    });
    
    if (roadmapResponse.data.success) {
      console.log('✅ Roadmap endpoint working!');
      console.log('Generated by:', roadmapResponse.data.generatedBy);
      console.log('Title:', roadmapResponse.data.data.title);
      console.log('Chapters:', roadmapResponse.data.data.chapters?.length);
    } else {
      console.log('❌ Roadmap endpoint failed');
    }
  } catch (error) {
    console.log('❌ Roadmap test failed:', error.message);
  }
}

testServerConnection().catch(console.error);
